-- 4. 각 사원의 급여액, 커미션, 급여 + 커미션 액수를 가져온다.
SELECT
    empno,
    sal                       급여액,
    comm                      커미션,
    sal + nvl(comm, 0)        "급여+커미션"
FROM
    emp;
------------------------------------------------------------------------------------------------
/*
커미션: 성과금
커미션은 null로 출력하면 안됩니다~ nvl을 사용해서 null의 값은 0으로 바꿔서 출력하기!
*/
--An)
SELECT
    sal,
    nvl(comm, 0),
    sal + nvl(comm, 0)
FROM
    emp;
------------------------------------------------------------------------------------------------


