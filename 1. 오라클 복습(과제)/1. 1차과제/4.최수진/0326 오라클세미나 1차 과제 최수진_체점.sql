-- 10. 10번 부서에서 근무하고 있는 직무가 MANAGER인 사원의 사원번호, 이름, 근무부서, 직무를 가져온다.
SELECT
    empno,
    ename,
    deptno,
    job
FROM
    emp
WHERE
    deptno = 10;
------------------------------------------------------------------------------------------------
/*
조건:10번 부서에서 근무하고 있는 직무가 MANAGER인

MANAGER조건이 빠졌습니다!
*/
--A)
SELECT
    empno,
    ename,
    deptno,
    job
FROM
    emp
WHERE
        deptno = 10
    AND job = upper('manager');
